export function assetsByCategory(): { category: string; count: number; value: number }[] {
  const map: Record<string, { count: number; value: number }> = {};
  assets.forEach((a) => {
    if (!map[a.category]) map[a.category] = { count: 0, value: 0 };
    map[a.category].count++;
    map[a.category].value += a.value;
  });
  return Object.entries(map).map(([category, data]) => ({ category, ...data }));
}
